/* while文の書き方！
初期値(var);
while(条件式){
処理内容 ; 増減式 ;
} */
var i =0;
while(i < 3){
document.write(i); i++ ;
}
var i =1;
while(i <= 3){
document.write(i); i++ ;
}
var i =5;
while(i > 0){
document.write(i); i-- ;
}
