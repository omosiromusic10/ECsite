/*加算子(インクリメント)　を使用する事で　１を自動的に足せるぞ！
①　前置加算子 / ++a
・・・変数の値に1を加えたモノを表示。
②　後置加算子 / a++
・・・変数の元の値を表示。その後に元の値に１を加える。 */
var a=10;
document.write(++a);

var b=10;
document.write(b++);

var b1=10;
document.write(b1++);

document.write(b);

var c =10;
document.write(c++);

document.write("<br>");

document.write(c++);

document.write("<br>");

document.write(c++);

/* 減算子(デクリメント)　を使用する事で　変数に１を自動的に引くよ。
①　前置減算子　/ --a
・・・変数の値に１を引いたモノを表示
②　後置減算子　/ a--
・・・変数の元の値を表示。その後に元の値に１を引く。 */

var c=10;
document.write(--c);

var c1=10;
document.write(c1--);

var d =10;
document.write(d--);

document.write("<br>");

document.write(d--);

document.write("<br>");

document.write(d--);